#include "libft.h"

int64_t		ft_get_snbr(const char **s)
{
	int64_t		n;
	bool		sign;

	while (**s == ' ' || (**s > 8 && **s < 14))
		++*s;
	sign = (**s == '-');
	if (**s == '+' || **s == '-')
		++*s;
	n = (ft_isdigit(**s)) ? (*(*s)++ - 48) : 0;
	while (ft_isdigit(**s))
		n = (n * 10) + *(*s)++ - 48;
	return (sign) ? -n : n;
}
